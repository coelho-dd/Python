# !!! INTRODUÇÃO À CONJUNTOS !!!

# -> A estrutura de conjuntos em Python são uma representação computacional dos conjuntos matemáticos. Isso significa que temos as operações clássicas que envolvem conjuntos na matemática, como união, diferença, intersecção, etc.

# Eis um exemplo de um conjunto:
conjunto1 = {1, 2, 3, 4, 5}
conjunto2 = {6, 7, 8, 9, 10}

# Operação de união é feita através do operador |:
uniao = conjunto1 | conjunto2
print(uniao)

# Operação de diferença é feita através do operador -:
diferenca = conjunto1 - conjunto2
print(diferenca)

# Operação de intersecção é feita através do operador &:
interseccao = conjunto1 & conjunto2
print(interseccao)


